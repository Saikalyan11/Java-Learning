package Operators;

public class Assignmentoperator {
    public static void main(String[] args) {

        // = operator
        int num1 = 30;
        System.out.println("= operator " + num1);

        // += operator
        int num2 = 1;
        num1 += num2;
        System.out.println("+= operator " + num1);

        // -= operator

        num1 -= num2;
        System.out.println("-= operator " + num1);

        // *=operator */
        num1 *= num2;
        System.out.println("*= operator " + num1);

        // /= operator
        num1 /= num2;
        System.out.println("/= operator " + num1);

        // %= operator
        num1 %= num2;
        System.out.println("%= operator " + num1);

    }
}
