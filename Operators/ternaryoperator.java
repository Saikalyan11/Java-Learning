public class ternaryoperator {
    public static void main(String[] args) {
        String n = "Hi";

        String result = n == "Hi" ? "yes" : "no";

        System.out.println(result);

        // to find even number
        int n1 = 20;

        int result1 = n1 % 2 == 0 ? 20 : 0;

        System.out.println(result1);
    }
}
