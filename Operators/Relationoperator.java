package Operators;

public class Relationoperator {
    public static void main(String[] args) {

        int var1 = 100;
        int var2 = 300;

        System.out.println("Is var1 = var2 " + (var1 == var2));
        System.out.println("Is var1 > var2 " + (var1 > var2));
        System.out.println("Is var1 < var2 " + (var1 < var2));
        System.out.println("Is var1 >= var2 " + (var1 >= var2));
        System.out.println("Is var1 <= var2 " + (var1 <= var2));
        System.out.println("Is var1 not equal to var2 " + (var1 != var2));
    }
}
