package Operators;

public class Arithmeticoperator {
    public static void main(String[] args) {

        // Addition operator
        int n1 = 30;
        int n2 = 40;
        int addresult = n1 + n2;

        // Substraction operator
        int subresult = n1 - n2;

        // Multiplication operator
        int mulresult = n1 * n2;

        // division operator
        int divresult = n2 / n1;

        // bitwise operator
        int bitresult = n2 % n1;

        System.out.println("this is add operator result " + addresult);
        System.out.println("this is sub operator result " + subresult);
        System.out.println("this is mul operator result " + mulresult);
        System.out.println("this is div operator result " + divresult);
        System.out.println("this is bit operator result " + bitresult);
    }
}