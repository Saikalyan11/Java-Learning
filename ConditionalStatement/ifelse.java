package ConditionalStatement;

public class ifelse {
    public static void main(String[] args) {
        int x = 8;

        if (x > 10 || x == 8) {
            System.out.println("Hello");
        } else {
            System.out.println("Bye");

        }

        if (x > 10 && x == 8) {
            System.out.println("Hello");
        } else {
            System.out.println("Bye");

        }

    }
}
